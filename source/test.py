def draw_maze(maze):
    cell_width = WIDTH // len(maze[0])
    cell_height = HEIGHT // len(maze)

    for row in range(len(maze)):
        for col in range(len(maze[row])):
            x = col * cell_width
            y = row * cell_height
            if maze[row][col] == 1:  # Tường
                pygame.draw.rect(WIN, BLACK, (x, y, cell_width, cell_height))
            elif maze[row][col] == 2:  # Điểm xuất phát
                pygame.draw.rect(WIN, GREEN, (x, y, cell_width, cell_height))
            elif maze[row][col] == 3:  # Điểm đích
                pygame.draw.rect(WIN, (255, 0, 0), (x, y, cell_width, cell_height))
            else: # Điểm trống
                pygame.draw.rect(WIN, WHITE, (x, y, cell_width, cell_height))



running = True
while running:
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
    
    # Vẽ ma trận mê cung và đường đi ở đây
    draw_maze(maze)
